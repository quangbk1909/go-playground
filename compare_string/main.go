func StripAccents(src string) (converted string) {
	if src == "" {
		return src
	}
	for _, c := range src {
		doneConvert := false
		for idx, accent := range accents {
			if c == accent {
				converted += string(replaceAccents[idx])
				doneConvert = true
				break
			}
		}
		if doneConvert {
			continue
		}
		converted += string(c)
	}
	return converted
}

func normalizeName(s string) string {
	reg, _ := regexp.Compile("[^a-z\\s]+")
	processedString := strings.ToLower(s)
	processedString = StripAccents(processedString)
	processedString = reg.ReplaceAllString(strings.Trim(processedString, " "), "")
	return processedString
}